#!/usr/bin/env node
/**
 * Image to ASCII converter for contact icons.
 *
 * Renders each contact slug at smaller width than the skills page (so they
 * sit comfortably inline in a row).
 *
 * Resolution order, per slug:
 *   1. static/logos/<slug>.{png,svg,jpg,jpeg}
 *   2. node_modules/devicon/icons/<deviconName>/<deviconName>-{plain,original}.svg
 *
 * Emits src/lib/contact-art.generated.ts containing { [slug]: asciiString }.
 */
import { readdirSync, writeFileSync, existsSync } from 'node:fs';
import { join, parse } from 'node:path';
import sharp from 'sharp';

const ROOT = new URL('..', import.meta.url).pathname;
const LOGOS_DIR = join(ROOT, 'static', 'logos');
const DEVICON_DIR = join(ROOT, 'node_modules', 'devicon', 'icons');
const OUT_FILE = join(ROOT, 'src', 'lib', 'contact-art.generated.ts');

const WIDTH = 18;
const ASPECT_CORRECT = 0.5;
const RAMP = ' .,:-=+*#%@';

const SLUGS = ['email', 'github', 'linkedin', 'instagram'];

const DEVICON_NAME = {
	email: null,
	github: 'github',
	linkedin: 'linkedin',
	instagram: null
};

const VARIANTS = ['original', 'plain', 'plain-wordmark', 'original-wordmark'];

async function imageToAscii(filePath) {
	const meta = await sharp(filePath).metadata();
	const srcAspect = (meta.height ?? 1) / (meta.width ?? 1);
	const height = Math.max(4, Math.round(WIDTH * srcAspect * ASPECT_CORRECT));
	const { data, info } = await sharp(filePath)
		.resize(WIDTH, height, { fit: 'fill' })
		.ensureAlpha()
		.raw()
		.toBuffer({ resolveWithObject: true });
	const ch = info.channels;
	const lines = [];
	for (let y = 0; y < height; y++) {
		let row = '';
		for (let x = 0; x < WIDTH; x++) {
			const i = (y * WIDTH + x) * ch;
			const a = data[i + 3] ?? 255;
			if (a < 64) {
				row += ' ';
				continue;
			}
			const lum = (data[i] + data[i + 1] + data[i + 2]) / 3;
			const idx = Math.min(
				RAMP.length - 1,
				Math.max(2, Math.round(((255 - lum) / 255) * (RAMP.length - 1)))
			);
			row += RAMP[idx];
		}
		lines.push(row.trimEnd());
	}
	return lines.join('\n');
}

function findLocalLogo(slug) {
	if (!existsSync(LOGOS_DIR)) return null;
	const files = readdirSync(LOGOS_DIR);
	const exts = ['svg', 'png', 'jpg', 'jpeg'];
	for (const ext of exts) {
		const match = files.find((f) => parse(f).name === slug && f.toLowerCase().endsWith('.' + ext));
		if (match) return join(LOGOS_DIR, match);
	}
	return null;
}

function findDevicon(slug) {
	const name = DEVICON_NAME[slug];
	if (!name) return null;
	const dir = join(DEVICON_DIR, name);
	if (!existsSync(dir)) return null;
	for (const variant of VARIANTS) {
		const p = join(dir, `${name}-${variant}.svg`);
		if (existsSync(p)) return p;
	}
	return null;
}

const result = {};
for (const slug of SLUGS) {
	const local = findLocalLogo(slug);
	const source = local ?? findDevicon(slug);
	if (!source) {
		console.log(`  skip:    ${slug}  (no source)`);
		continue;
	}
	const art = await imageToAscii(source);
	result[slug] = art;
	const label = local ? 'local' : 'devicon';
	console.log(`  ${label}: ${slug}`);
}

const ts = `// AUTO-GENERATED by scripts/build-contact-art.mjs. Do not edit by hand.
// To update: drop a logo into static/logos/<slug>.{svg,png} and run \`node scripts/build-contact-art.mjs\`.

export const contactArt: Record<string, string> = ${JSON.stringify(result, null, 2)};
`;

writeFileSync(OUT_FILE, ts);
console.log(`\nwrote ${OUT_FILE}  (${Object.keys(result).length} of ${SLUGS.length} icon(s))`);
